package datatype_and_variables;

public class Variables {
	static int a =10;
	public static void main(String[] args) {
		int a =20;
		System.out.println(a);  //20
		
		byte b =127;
		long l = 4577777777L;
		short s = 234;
		
		float f = 10.26f;
		double d = 32.456d;
		boolean bool = true;
		char ch ='@';
		String str = "Hello";
	}
	public static void access()
	{
		System.out.println(a);  //10
	}

}
